use crate::rtnl;

#[test]
fn linkinfo() -> Result<(), String> {
    let orgraw = [
        0x94, 0x00, //
        0x12, 0x00, // IFLA_LINKINFO
        0x0b, 0x00, //
        0x01, 0x00, // IFLA_INFO_KIND
        0x69, 0x70, 0x36, 0x74, 0x6e, 0x6c, 0x00, 0x00, //
        0x84, 0x00, //
        0x02, 0x00, // IFLA_INFO_DATA
        0x08, 0x00, //
        0x01, 0x00, // IFLA_IPTUN_LINK
        0x00, 0x00, 0x00, 0x00, //
        0x14, 0x00, //
        0x02, 0x00, // IFLA_IPTUN_LOCAL
        0x20, 0x01, 0x0d, 0xb8, 0x00, 0x00, 0x00, 0x04, //
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01, //
        0x14, 0x00, //
        0x03, 0x00, // IFLA_IPTUN_REMOTE
        0x20, 0x01, 0x0d, 0xb8, 0x00, 0x00, 0x00, 0x04, //
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x11, //
        0x05, 0x00, //
        0x04, 0x00, // IFLA_IPTUN_TTL
        0x40, 0x00, 0x00, 0x00, //
        0x05, 0x00, //
        0x06, 0x00, // IFLA_IPTUN_ENCAP_LIMIT
        0x04, 0x00, 0x00, 0x00, //
        0x08, 0x00, //
        0x07, 0x00, // IFLA_IPTUN_FLOWINFO
        0x00, 0x00, 0x00, 0x00, //
        0x08, 0x00, //
        0x08, 0x00, // IFLA_IPTUN_FLAGS
        0x01, 0x00, 0x03, 0x00, //
        0x05, 0x00, //
        0x09, 0x00, // IFLA_IPTUN_PROTO
        0x04, 0x00, 0x00, 0x00, //
        0x08, 0x00, //
        0x14, 0x00, // IFLA_IPTUN_FWMARK
        0x00, 0x00, 0x00, 0x00, //
        0x06, 0x00, //
        0x0f, 0x00, // IFLA_IPTUN_ENCAP_TYPE
        0x00, 0x00, 0x00, 0x00, //
        0x06, 0x00, //
        0x11, 0x00, // IFLA_IPTUN_ENCAP_SPORT
        0x00, 0x00, 0x00, 0x00, //
        0x06, 0x00, //
        0x12, 0x00, // IFLA_IPTUN_ENCAP_DPORT
        0x00, 0x00, 0x00, 0x00, //
        0x06, 0x00, //
        0x10, 0x00, // IFLA_IPTUN_ENCAP_FLAGS
        0x00, 0x00, 0x00, 0x00, //
    ];
    let resmsg = match rtnl::packets::LinkinfoAttr::parse(&orgraw) {
        Ok(resmsg) => resmsg,
        Err(e) => return Err(format!("parse error {}", e)),
    };
    let mut resraw = [0u8; 148];
    if let Err(e) = resmsg.serialize(&mut resraw) {
        return Err(format!("serialize error {}", e));
    };
    if resraw != orgraw {
        return Err(format!("resraw not eq"));
    }
    Ok(())
}
